    async def _eval_column_alignment(
        self,
        spec: VerificationTestSpec,
        sql: str,
        exec_result: ExecutionResult,
    ) -> VerificationTestResult:
        """Column alignment: LLM judges whether SELECT columns answer the question.

        This is an expensive test — makes 1 LLM call (model_fast).
        Runs on every fix iteration (run_expensive is always True in QueryFixer).
        """
        logger.debug("Running expensive test: column_alignment")
        select_match = re.search(
            r"SELECT\s+(.*?)\s+FROM\b",
            sql,
            re.IGNORECASE | re.DOTALL,
        )
        select_clause = select_match.group(1).strip() if select_match else sql[:200]
        sample_row = str(exec_result.rows[0]) if exec_result.rows else "(empty result)"

        prompt = (
            f"Verification: Column Alignment Check\n"
            f"Description: {spec.description}\n\n"
            f"SELECT clause: {select_clause}\n"
            f"Sample result row: {sample_row}\n\n"
            f"Expected outcome: {spec.expected_outcome}\n\n"
            "Do the selected columns actually answer the question? "
            "Reply with exactly 'PASS' or 'FAIL' on the first line, then a brief explanation."
        )

        try:
            client = get_client()
            response = await client.generate(
                model=settings.model_fast,
                system=[CacheableText(text="You are an SQL quality analyst.", cache=False)],
                messages=[{"role": "user", "content": prompt}],
                tools=[],
                temperature=0.0,
                max_tokens=200,
            )
            judgment = (response.text or "").strip().upper()
            if judgment.startswith("PASS"):
                logger.debug("column_alignment: PASS — %s", (response.text or "")[:100])
                return VerificationTestResult(
                    test_type="column_alignment",
                    status="pass",
                    actual_outcome=f"LLM judgment: {(response.text or '')[:200]}",
                    is_critical=spec.is_critical,
                )
            elif judgment.startswith("FAIL"):
                logger.info(
                    "column_alignment: FAIL — %s", (response.text or "")[:200]
                )
                return VerificationTestResult(
                    test_type="column_alignment",
                    status="fail",
                    actual_outcome=f"LLM judgment: {(response.text or '')[:200]}",
                    is_critical=spec.is_critical,
                )
            logger.warning(
                "column_alignment: inconclusive LLM response — %s",
                (response.text or "")[:100],
            )
            return VerificationTestResult(
                test_type="column_alignment",
                status="skip",
                actual_outcome=f"Inconclusive LLM response: {(response.text or '')[:100]}",
                is_critical=spec.is_critical,
            )
        except LLMError as exc:
            logger.warning("column_alignment LLM call failed: %s", exc)
            return VerificationTestResult(
                test_type="column_alignment",
                status="error",
                actual_outcome=f"LLM error: {exc}",
                is_critical=spec.is_critical,
            )

    async def _eval_boundary(
        self,
        spec: VerificationTestSpec,
        sql: str,
    ) -> VerificationTestResult:
        """Boundary test: LLM checks if date/time constraints match the question.

        This is an expensive test — makes 1 LLM call (model_fast).
        Runs on every fix iteration (run_expensive is always True in QueryFixer).
        """
        logger.debug("Running expensive test: boundary")
        where_match = re.search(
            r"\bWHERE\b(.*?)(?:\bGROUP\s+BY\b|\bORDER\s+BY\b|\bHAVING\b|\bLIMIT\b|$)",
            sql,
            re.IGNORECASE | re.DOTALL,
        )
        where_clause = where_match.group(1).strip() if where_match else "(no WHERE clause)"

        prompt = (
            f"Verification: Boundary/Date Check\n"
            f"Description: {spec.description}\n\n"
            f"WHERE clause: {where_clause}\n\n"
            f"Expected outcome: {spec.expected_outcome}\n\n"
            "Do the date/time/boundary conditions match what the question asks for? "
            "Reply with exactly 'PASS' or 'FAIL' on the first line, then a brief explanation."
        )

        try:
            client = get_client()
            response = await client.generate(
                model=settings.model_fast,
                system=[CacheableText(text="You are an SQL quality analyst.", cache=False)],
                messages=[{"role": "user", "content": prompt}],
                tools=[],
                temperature=0.0,
                max_tokens=200,
            )
            judgment = (response.text or "").strip().upper()
            if judgment.startswith("PASS"):
                status = "pass"
                logger.debug("boundary: PASS — %s", (response.text or "")[:100])
            elif judgment.startswith("FAIL"):
                status = "fail"
                logger.info("boundary: FAIL — %s", (response.text or "")[:200])
            else:
                status = "skip"
                logger.warning(
                    "boundary: inconclusive LLM response — %s",
                    (response.text or "")[:100],
                )
            return VerificationTestResult(
                test_type="boundary",
                status=status,
                actual_outcome=f"LLM judgment: {(response.text or '')[:200]}",
                is_critical=spec.is_critical,
            )
        except LLMError as exc:
            logger.warning("boundary LLM call failed: %s", exc)
            return VerificationTestResult(
                test_type="boundary",
                status="error",
                actual_outcome=f"LLM error: {exc}",
                is_critical=spec.is_critical,
            )

    def _eval_symmetry(
        self,
        spec: VerificationTestSpec,
        exec_result: ExecutionResult,
        db_path: str,
    ) -> VerificationTestResult:
        """Symmetry test: verify an aggregate total equals the sum of sub-groups.

        Classified as an expensive test and runs on every fix iteration alongside
        column_alignment and boundary (run_expensive is always True in QueryFixer).
        Unlike column_alignment and boundary, this test makes NO LLM calls — it
        executes a complementary SQL query against the raw database and compares
        the first numeric value to the candidate result within a 1% tolerance.

        When to use: questions like "how many total X?" where the LLM plan can
        supply a verification_sql that computes the expected total a different way
        (e.g. sum of sub-groups). Applicable to roughly <10% of BIRD questions.

        Requires spec.verification_sql to be set; returns "skip" otherwise.
        """
        logger.debug("Running expensive test: symmetry")

        if not spec.verification_sql:
            logger.debug("symmetry: skip — no verification_sql provided")
            return VerificationTestResult(
                test_type="symmetry",
                status="skip",
                actual_outcome="No verification_sql provided for symmetry check.",
                is_critical=spec.is_critical,
            )

        vr = execute_sql(db_path, spec.verification_sql)
        if not vr.success or not vr.rows:
            logger.warning(
                "symmetry: verification SQL failed — %s", vr.error
            )
            return VerificationTestResult(
                test_type="symmetry",
                status="error",
                actual_outcome=f"Symmetry verification SQL failed: {vr.error}",
                is_critical=spec.is_critical,
            )

        # Extract first numeric value from the candidate result
        main_val: Optional[float] = None
        for val in (exec_result.rows[0] if exec_result.rows else []):
            if isinstance(val, (int, float)):
                main_val = float(val)
                break

        # Extract first numeric value from the symmetry verification result
        check_val: Optional[float] = None
        for val in vr.rows[0]:
            if isinstance(val, (int, float)):
                check_val = float(val)
                break

        if main_val is None or check_val is None:
            logger.debug(
                "symmetry: skip — could not extract numeric values "
                "(main_val=%s, check_val=%s)", main_val, check_val
            )
            return VerificationTestResult(
                test_type="symmetry",
                status="skip",
                actual_outcome="Could not extract numeric values for symmetry comparison.",
                is_critical=spec.is_critical,
            )

        tolerance = max(abs(main_val) * 0.01, 0.01)
        if abs(main_val - check_val) <= tolerance:
            logger.debug(
                "symmetry: PASS — main=%s matches check=%s (tolerance=%.4f)",
                main_val, check_val, tolerance,
            )
            return VerificationTestResult(
                test_type="symmetry",
                status="pass",
                actual_outcome=(
                    f"Main result {main_val} matches symmetry check {check_val}."
                ),
                is_critical=spec.is_critical,
            )

        logger.info(
            "symmetry: FAIL — main=%s, check=%s, delta=%.4f (tolerance=%.4f). "
            "Result may be overcounting or undercounting.",
            main_val, check_val, abs(main_val - check_val), tolerance,
        )
        return VerificationTestResult(
            test_type="symmetry",
            status="fail",
            actual_outcome=(
                f"Symmetry mismatch: main={main_val}, check={check_val}. "
                "Result may be overcounting or undercounting."
            ),
            is_critical=spec.is_critical,
        )

    # ------------------------------------------------------------------
    # Confidence adjustment
    # ------------------------------------------------------------------

    def _compute_adjustment(
        self,
        results: list[VerificationTestResult],
    ) -> float:
        """Compute confidence score adjustment from test results."""
        if not results:
            return 0.0

        adjustment = 0.0
        for r in results:
            if r.status == "fail":
                adjustment += _PENALTY_CRITICAL if r.is_critical else _PENALTY_MINOR

        # Bonus if all non-error tests passed
        non_error = [r for r in results if r.status != "error"]
        if non_error and all(r.status in ("pass", "skip") for r in non_error):
            passed = [r for r in non_error if r.status == "pass"]
            if passed:
                adjustment = min(adjustment + _BONUS_ALL_PASS, _MAX_BONUS)

        return adjustment
