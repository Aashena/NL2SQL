class QueryVerifier:
    """
    Generates and evaluates semantic verification plans for SQL candidates.

    Workflow::

        verifier = QueryVerifier()

        # Once per question:
        specs = await verifier.generate_plan(question, evidence, schema)

        # Per candidate, per fix iteration:
        eval_result = await verifier.evaluate_candidate(
            specs, candidate_id, sql, exec_result, db_path,
            run_expensive=(iteration == beta)  # True only on final check
        )
    """

    async def generate_plan(
        self,
        question: str,
        evidence: str,
        schema: str,
    ) -> list[VerificationTestSpec]:
        """
        Generate applicable verification tests for this question.

        Makes exactly 1 LLM call (model_powerful). Returns an empty list on any
        failure — verification is optional and never blocks the fix loop.
        """
        q = sanitize_prompt_text(question)
        ev = sanitize_prompt_text(evidence or "None")
        schema_abbrev = schema[:3000]  # abbreviate for very large schemas

        user_msg = (
            f"Question: {q}\n"
            f"Evidence: {ev}\n\n"
            f"Database Schema:\n{schema_abbrev}\n\n"
            "Generate the verification tests for this question."
        )

        try:
            client = get_client()
            response = await client.generate(
                model=settings.model_powerful,
                system=[_PLAN_SYSTEM],
                messages=[{"role": "user", "content": user_msg}],
                tools=[_PLAN_TOOL],
                tool_choice_name="verification_plan",
                temperature=0.0,
                max_tokens=1500,
            )
            if not response.tool_inputs:
                logger.warning("Verification plan LLM returned no tool inputs")
                return []
            raw_tests = response.tool_inputs[0].get("tests", [])
            specs: list[VerificationTestSpec] = []
            for t in raw_tests:
                try:
                    spec = VerificationTestSpec(**t)
                    if spec.test_type in _ALL_TEST_TYPES:
                        specs.append(spec)
                    else:
                        logger.warning("Unknown test_type %r in plan — skipped", spec.test_type)
                except Exception as e:
                    logger.warning("Failed to parse VerificationTestSpec: %s", e)
            return specs
        except LLMError as exc:
            logger.warning("Verification plan generation failed (LLMError): %s", exc)
            return []
        except Exception as exc:
            logger.warning("Unexpected error in generate_plan: %s", exc)
            return []

    async def evaluate_candidate(
        self,
        specs: list[VerificationTestSpec],
        candidate_id: str,
        sql: str,
        exec_result: ExecutionResult,
        db_path: str,
        run_expensive: bool = False,
    ) -> VerificationEvaluation:
        """
        Evaluate all applicable specs against a SQL candidate.

        Parameters
        ----------
        specs:
            Tests generated by generate_plan().
        candidate_id:
            Generator ID for logging.
        sql:
            Current SQL (after any preceding fix iterations).
        exec_result:
            Latest execution result for this SQL.
        db_path:
            Path to the SQLite database for running verification queries.
        run_expensive:
            If True, also run LLM-judgment tests (column_alignment, boundary,
            symmetry). Always True — parameter kept for interface compatibility.
        """
        results: list[VerificationTestResult] = []

        for spec in specs:
            is_expensive = spec.test_type in _EXPENSIVE_TESTS
            if is_expensive and not run_expensive:
                continue  # Skip expensive tests on early iterations

            if not exec_result.success:
                # Cannot evaluate semantic tests without a valid execution result
                results.append(VerificationTestResult(
                    test_type=spec.test_type,
                    status="skip",
                    actual_outcome="Skipped — SQL did not execute successfully.",
                    is_critical=spec.is_critical,
                ))
                continue

            result = await self._evaluate_single(spec, sql, exec_result, db_path)
            results.append(result)

        all_pass = all(r.status in ("pass", "skip", "error") for r in results)
        adjustment = self._compute_adjustment(results)
        failure_hints = [
            f"{r.test_type.upper()} TEST FAILED: {r.actual_outcome}  "
            f"Hint: {self._get_hint(r.test_type, specs)}"
            for r in results if r.status == "fail"
        ]

        return VerificationEvaluation(
            candidate_id=candidate_id,
            test_results=results,
            all_pass=all_pass,
            confidence_adjustment=adjustment,
            failure_hints=failure_hints,
        )

    def _get_hint(self, test_type: str, specs: list[VerificationTestSpec]) -> str:
        for spec in specs:
            if spec.test_type == test_type:
                return spec.fix_hint
        return "Review the SQL logic."

    async def _evaluate_single(
        self,
        spec: VerificationTestSpec,
        sql: str,
        exec_result: ExecutionResult,
        db_path: str,
    ) -> VerificationTestResult:
        """Dispatch to the appropriate evaluation method."""
        tt = spec.test_type
        try:
            if tt == "grain":
                return self._eval_grain(spec, exec_result, db_path)
            elif tt == "null":
                return self._eval_null(spec, exec_result)
            elif tt == "duplicate":
                return self._eval_duplicate(spec, exec_result, db_path)
            elif tt == "ordering":
                return self._eval_ordering(spec, sql)
            elif tt == "scale":
                return self._eval_scale(spec, exec_result)
            elif tt == "column_alignment":
                return await self._eval_column_alignment(spec, sql, exec_result)
            elif tt == "boundary":
                return await self._eval_boundary(spec, sql)
            elif tt == "symmetry":
                return self._eval_symmetry(spec, exec_result, db_path)
            else:
                return VerificationTestResult(
                    test_type=tt,
                    status="skip",
                    actual_outcome=f"Unknown test type: {tt}",
                    is_critical=spec.is_critical,
                )
        except Exception as exc:
            logger.warning("Error evaluating %r test for candidate: %s", tt, exc)
            return VerificationTestResult(
                test_type=tt,
                status="error",
                actual_outcome=f"Evaluation error: {exc}",
                is_critical=spec.is_critical,
            )

    # ------------------------------------------------------------------
    # Cheap test implementations
    # ------------------------------------------------------------------

    def _eval_grain(
        self,
        spec: VerificationTestSpec,
        exec_result: ExecutionResult,
        db_path: str,
    ) -> VerificationTestResult:
        """Grain test: check result row count is within [lower, upper] and
        optionally that column count matches expected."""
        actual_count = len(exec_result.rows)

        # ── 1. Resolve upper bound ──────────────────────────────────────────
        upper: Optional[int] = None

        if spec.verification_sql_upper:
            vr = execute_sql(db_path, spec.verification_sql_upper)
            if vr.success and vr.rows and isinstance(vr.rows[0][0], (int, float)):
                upper = int(vr.rows[0][0])
        elif spec.verification_sql:
            # Backward compat: old verification_sql treated as upper bound for grain
            vr = execute_sql(db_path, spec.verification_sql)
            if vr.success and vr.rows and isinstance(vr.rows[0][0], (int, float)):
                upper = int(vr.rows[0][0])
        elif spec.row_count_max is not None:
            upper = spec.row_count_max
        elif spec.expected_row_count is not None:
            # Legacy exact-match: treat as upper = expected
            upper = spec.expected_row_count

        # ── 2. Resolve lower bound (minimum is always 1) ───────────────────
        lower: int = max(spec.row_count_min if spec.row_count_min is not None else 1, 1)

        # ── 3. Row count check ─────────────────────────────────────────────
        row_count_ok: Optional[bool] = None
        row_count_msg: str = ""

        if upper is not None:
            if lower <= actual_count <= upper:
                row_count_ok = True
                row_count_msg = (
                    f"Row count {actual_count} is within range [{lower}, {upper}]."
                )
            elif actual_count < lower:
                row_count_ok = False
                row_count_msg = (
                    f"Row count {actual_count} is below minimum {lower}. "
                    "Result may be too restrictive or missing rows."
                )
            else:
                row_count_ok = False
                row_count_msg = (
                    f"Row count {actual_count} exceeds upper bound {upper}. "
                    "Possible wrong grain or missing GROUP BY / DISTINCT."
                )
        else:
            # No upper bound — only enforce lower
            if actual_count >= lower:
                row_count_ok = True
                row_count_msg = f"Row count {actual_count} meets minimum {lower}."
            else:
                row_count_ok = False
                row_count_msg = (
                    f"Row count {actual_count} is below minimum {lower} "
                    "(empty results are always wrong)."
                )

        # ── 4. Column count check (independent of row count) ───────────────
        col_count_ok: Optional[bool] = None
        col_count_msg: str = ""

        if spec.expected_column_count is not None and exec_result.rows:
            actual_cols = len(exec_result.rows[0])
            if actual_cols == spec.expected_column_count:
                col_count_ok = True
                col_count_msg = (
                    f"Column count {actual_cols} matches expected "
                    f"{spec.expected_column_count}."
                )
            else:
                col_count_ok = False
                col_count_msg = (
                    f"Column count {actual_cols} does not match expected "
                    f"{spec.expected_column_count}. Check SELECT list."
                )

        # ── 5. Combine results ─────────────────────────────────────────────
        if row_count_ok is None and col_count_ok is None:
            return VerificationTestResult(
                test_type="grain",
                status="skip",
                actual_outcome="No grain bounds or expected column count specified.",
                is_critical=spec.is_critical,
            )

        row_fail = row_count_ok is False
        col_fail = col_count_ok is False

        if row_fail or col_fail:
            parts = []
            if row_fail:
                parts.append(row_count_msg)
            if col_fail:
                parts.append(col_count_msg)
            return VerificationTestResult(
                test_type="grain",
                status="fail",
                actual_outcome=" | ".join(parts),
                is_critical=spec.is_critical,
            )

        parts = []
        if row_count_ok is True:
            parts.append(row_count_msg)
        if col_count_ok is True:
            parts.append(col_count_msg)
        return VerificationTestResult(
            test_type="grain",
            status="pass",
            actual_outcome=" | ".join(parts),
            is_critical=spec.is_critical,
        )

    def _eval_null(
        self,
        spec: VerificationTestSpec,
        exec_result: ExecutionResult,
    ) -> VerificationTestResult:
        """Null test: check result rows for unexpected NULL values."""
        rows = exec_result.rows
        if not rows:
            return VerificationTestResult(
                test_type="null",
                status="skip",
                actual_outcome="No rows to check for NULLs.",
                is_critical=spec.is_critical,
            )

        null_count = sum(1 for row in rows for val in row if val is None)
        total_cells = sum(len(row) for row in rows)

        if null_count == 0:
            return VerificationTestResult(
                test_type="null",
                status="pass",
                actual_outcome=f"No NULL values in {len(rows)} result rows.",
                is_critical=spec.is_critical,
            )
        null_pct = (null_count / total_cells * 100) if total_cells > 0 else 0
        return VerificationTestResult(
            test_type="null",
            status="fail",
            actual_outcome=(
                f"Found {null_count} NULL values across {len(rows)} rows "
                f"({null_pct:.1f}% of cells). May indicate missing JOIN conditions."
            ),
            is_critical=spec.is_critical,
        )

    def _eval_duplicate(
        self,
        spec: VerificationTestSpec,
        exec_result: ExecutionResult,
        db_path: str,
    ) -> VerificationTestResult:
        """Duplicate test: detect JOIN-induced row multiplication."""
        actual_count = len(exec_result.rows)

        if spec.verification_sql:
            vr = execute_sql(db_path, spec.verification_sql)
            if vr.success and vr.rows and isinstance(vr.rows[0][0], (int, float)):
                expected = int(vr.rows[0][0])
                if expected > 0:
                    ratio = actual_count / expected
                    if ratio >= 2.0:
                        return VerificationTestResult(
                            test_type="duplicate",
                            status="fail",
                            actual_outcome=(
                                f"Result has {actual_count} rows but expected ~{expected} "
                                f"distinct entities (ratio: {ratio:.1f}x). "
                                "JOIN may be multiplying rows."
                            ),
                            is_critical=spec.is_critical,
                        )
                    return VerificationTestResult(
                        test_type="duplicate",
                        status="pass",
                        actual_outcome=(
                            f"Row count {actual_count} vs expected {expected} "
                            "— no JOIN multiplication detected."
                        ),
                        is_critical=spec.is_critical,
                    )

        # Fallback: check for exact duplicate rows in result
        if exec_result.rows:
            try:
                unique_rows = len(set(exec_result.rows))
            except TypeError:
                # Rows contain unhashable types (e.g. lists) — skip
                return VerificationTestResult(
                    test_type="duplicate",
                    status="skip",
                    actual_outcome="Result rows contain unhashable values — skipped.",
                    is_critical=spec.is_critical,
                )
            if unique_rows < actual_count:
                dup_count = actual_count - unique_rows
                return VerificationTestResult(
                    test_type="duplicate",
                    status="fail",
                    actual_outcome=(
                        f"Found {dup_count} duplicate rows ({actual_count} total, "
                        f"{unique_rows} unique). JOIN may be creating duplicates."
                    ),
                    is_critical=spec.is_critical,
                )
            return VerificationTestResult(
                test_type="duplicate",
                status="pass",
                actual_outcome=f"All {actual_count} rows are unique — no duplicates.",
                is_critical=spec.is_critical,
            )

        return VerificationTestResult(
            test_type="duplicate",
            status="skip",
            actual_outcome="No rows to check for duplicates.",
            is_critical=spec.is_critical,
        )

    def _eval_ordering(
        self,
        spec: VerificationTestSpec,
        sql: str,
    ) -> VerificationTestResult:
        """Ordering test: structural check that required SQL clauses are present."""
        required = spec.required_sql_keywords or ["ORDER BY", "LIMIT"]
        sql_upper = sql.upper()
        missing = [kw for kw in required if kw.upper() not in sql_upper]

        if not missing:
            return VerificationTestResult(
                test_type="ordering",
                status="pass",
                actual_outcome=f"All required clauses present: {required}.",
                is_critical=spec.is_critical,
            )
        return VerificationTestResult(
            test_type="ordering",
            status="fail",
            actual_outcome=f"Missing required SQL clauses: {missing}.",
            is_critical=spec.is_critical,
        )

    def _eval_scale(
        self,
        spec: VerificationTestSpec,
        exec_result: ExecutionResult,
    ) -> VerificationTestResult:
        """Scale test: check that numeric result values are within expected range."""
        if spec.numeric_min is None and spec.numeric_max is None:
            return VerificationTestResult(
                test_type="scale",
                status="skip",
                actual_outcome="No numeric range specified.",
                is_critical=spec.is_critical,
            )

        rows = exec_result.rows
        if not rows:
            return VerificationTestResult(
                test_type="scale",
                status="skip",
                actual_outcome="No rows to check.",
                is_critical=spec.is_critical,
            )

        violations: list[str] = []
        for row in rows:
            for val in row:
                if not isinstance(val, (int, float)):
                    continue
                if spec.numeric_min is not None and val < spec.numeric_min:
                    violations.append(f"{val} < min({spec.numeric_min})")
                if spec.numeric_max is not None and val > spec.numeric_max:
                    violations.append(f"{val} > max({spec.numeric_max})")

        if not violations:
            bounds = []
            if spec.numeric_min is not None:
                bounds.append(f"min={spec.numeric_min}")
            if spec.numeric_max is not None:
                bounds.append(f"max={spec.numeric_max}")
            return VerificationTestResult(
                test_type="scale",
                status="pass",
                actual_outcome=(
                    f"All numeric values within expected range ({', '.join(bounds)})."
                ),
                is_critical=spec.is_critical,
            )
        sample = violations[:3]
        return VerificationTestResult(
            test_type="scale",
            status="fail",
            actual_outcome=(
                f"Out-of-range values: {sample}. May indicate wrong column or unit."
            ),
            is_critical=spec.is_critical,
        )

